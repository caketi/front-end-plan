// 描述：对象类别


// `Ordinary objects` Have all the default internal behaviors for objects in JavaScript.

// `Exotic objects` Have internal behavior that differs from the default in some way.

// `Standard objects` Defned by ECMAScript 6, such as Array, Date, and so on.
// Standard objects can be ordinary or exotic.

// `Built-in` objects Present in a JavaScript execution environment when a
// script begins to execute. All standard objects are built­in objects.
